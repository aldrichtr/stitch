Describe "Testing private function Format-ChangelogRelease" -Tags @('unit', 'ChangelogRelease', 'Format' ) {
    Context 'The Format-ChangelogRelease command is available and is valid' {
        BeforeAll {
            $command = Get-Command 'Format-ChangelogRelease'
            $tokens, $errors = @()
            $parsed = [System.Management.Automation.Language.Parser]::ParseFile($sourceFile, [ref]$tokens, [ref]$errors)

        }

        It 'The source file should exist' {
            $sourceFile | Should -Exist
